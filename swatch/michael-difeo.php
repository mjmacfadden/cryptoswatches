        <p><em>The following is an artist profile featuring</em>&nbsp;<a title="@michaeldifeo" href="https://twitter.com/michaeldifeo" target="_blank" rel="noopener"><strong>@</strong><strong>michaeldifeo</strong></a><em><strong>.</strong></em></p>
        <p><strong>Please introduce yourself to our readers. What would you like them to know about you?</strong></p>
        <p>I am a professor of media arts, a commercial still life photographer,&nbsp;and an artist in residence at Art Beyond Sight. I am located in&nbsp;Jersey City, NJ.</p>
        <p><strong>What was the inspiration for this piece?</strong></p>
        <p>At the time I made this piece, I was hanging out at an after hours&nbsp;party &amp; art space in Jersey City (pre-pandemic of course). There were&nbsp;some interesting atmospheric lights set up which were perfect for a&nbsp;&ldquo;camera painting&rdquo;. It was a way for me to participate in the social&nbsp;setting in a comfortable, creative way. A lot of my work is in&nbsp;dialogue with the limits of perception and the nature of reality which&nbsp;is where the title &ldquo;String Theory&rdquo; comes from.</p>
        <p><strong>How long have you been taking photos and creating in the digital realm?</strong></p>
        <p>I probably started with digital photography and art in about 2003.</p>
        <img src="img/michaeldifeo_collaboration.jpg" class="img-fluid my-4" alt="Michael Difeo Collaboration">
        <p><strong>Can you tell us about your workflow?</strong></p>
        <p>My workflow changes a lot depending on the project. For this image,&nbsp;it was created in camera using a long exposure&hellip;probably about a 10&nbsp;second one. During the exposure I moved the camera around to create&nbsp;the &ldquo;strings&rdquo; of lights, using a type of motion blur technique called&nbsp;&ldquo;camera painting&rdquo;. There was minimal processing in photoshop, the&nbsp;only thing I did was invert the colors.</p>
        <p><strong>Who are your artistic/photographic influences?</strong></p>
        <p>Rothko, Pollock, and Brakhage to name a few. I generally relate more&nbsp;to painters than photographers.</p>
        <p><strong>What got you interested in NFTs/Crypto Art/Digital Collectibles?</strong></p>
        <p>Once I understood that NFTs solved the issue of proving digital art&nbsp;authentic, It was a really exciting revelation. As I discovered more&nbsp;of this ultra engaged community around digital art, I became even more&nbsp;involved.</p>
