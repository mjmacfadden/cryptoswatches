        <p>This piece was inspired by our new Duck Butt Collective collection, and also a reflection of everything we&rsquo;ve kept inside during COVID (literally being isolated inside the house!). So we stuffed the Duck with everything that&rsquo;s been on our minds lately, kind of like a narrative turducken&hellip;</p>
