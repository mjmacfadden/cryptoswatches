        <h4>by Michael Macfadden</h4>
        <p><em>The following is an artist profile featuring&nbsp;<strong><a href="https://twitter.com/ryanartwalker" target="_blank" rel="noopener">@ryanartwalker</a></strong>, the creator of the piece in this series.</em></p>
        <p><strong>Please introduce yourself to our readers. What would you like them to know about you?</strong></p>
        <p>Greetings everyone, my name is Ryan Arthur Walker, &ldquo;arthur&rdquo; in many of my artistic projects. I am originally for Long Island, NY but currently I live and work as a teacher in Shanghai, China.</p>
        <p><strong>What was the inspiration for this piece?</strong></p>
        <p>I love the way film photos look and recently I have become obsessed with learning more about video synthesis. The combination of the two creates this old school/new school collab that can enhance the photos as artistic expressions. I&rsquo;ve been trying to explore this idea as much as possible and grow with each piece.</p>
        <p><strong>Can you tell us about your workflow (how to you come up with ideas, what tools do you use to create)?</strong></p>
        <p>I have a variety of film cameras but I have fallen in love with my Rolleicord. I had previously never shot a medium format camera but after using the Rolleicord I never want to go back. When I shoot with my Ricoh Half frame or variety of SLRs, Kentmere 400 is my go to film. Black and white is emotional and I love it&rsquo;s look. I use the visual synthesis program, LUMEN, then I combine the layers of photos and videos in photoshop and premiere pro. I like to set a timer of 30 minutes or less to give myself a box to work within, that&rsquo;s usually when I feel the most creative.&nbsp;</p>
        <p><strong>How long have you been designing in the digital realm?</strong></p>
        <p>I&rsquo;ve been posting photos and videos online in places like Facebook, Instagram and youtube since 2010. Ranging anywhere from sport photography, portraits, parkour, sketches and travel. I&rsquo;ve always found digital art to be something that most interested me. Started graphic art on Adobe Photoshop 6 in high school and fell in love. Almost 15 years later and I still have a deep love and passion for it. Always learning and trying to discover new was to express myself using art is what let me to discovering NFTs.&nbsp;</p>
        <p><strong>Who are your artistic influences?</strong></p>
        <p>One of my first inspirations is my cousin, who creates videos for professional skate companies. One of the videos he showed me when I was kid was so inspirational to me I still think about it from time to time.&nbsp;</p>
        <p><strong>What got you interested in NFTs/Crypto Art/Digital Collectibles?</strong></p>
        <p>The idea of creating art that can transferred to people and deliver an ownership element is exciting. I am a collector type person by nature and have spent 15 years collecting records, tapes and cards. To be able to collect art digitally feels natural to me. I know this is the beginning of something that will change the way we fundamentally use the Internet in the next 50 years and I want to get in on the ground floor.</p>
