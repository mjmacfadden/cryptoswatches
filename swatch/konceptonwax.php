        <p><strong>Please introduce yourself to our readers. What would you like them to know about you?</strong></p>
        <p>Hi everyone! My name is Stuart and I do Konceptonwax. I have been drawing on and off all my life, but I started to draw anime back in highschool. Around the time I entered college, I stopped drawing to focus on my degree. Half way through my third year, I picked up drawing again. This time, however, my focus was on concept art for games. At the time, I was not happy with what I was studying (graphic, web, and media design) and figured that I could try to get a job as a junior concept artist and took a few art courses online.</p>
        <p>However, being in South Africa, we do not have many game design companies, and I thought with my lack of experience, I should just focus on getting a job that my degree prepared me for. It wasn&rsquo;t until January 2021 that I picked up a pencil again to draw. This time, I had a vision of how I was going to do it. I have always wanted to create my own YouTube channel, similar to Kienan Lafferty (he was one of my favourite artist growing up), and I figured if I could build up a few followers on WAX, I could eventually start my own YouTube, Gumroad and Patreon and help like minded individuals achieve their dreams.</p>
        <p><strong>What was the inspiration for this piece?</strong></p>
        <p>I have wanted to create something cute and light hearted for a while now. I had this idea in the back of my mind, and I decided to give it a go. I had been focusing on my &lsquo;Girls Of Ink&rsquo; series, and I think this helped give me a break, before I continue with my final illustration for that series.</p>
        <p><strong>Can you tell us about your workflow (how do you come up with ideas, what tools do you use to create)?</strong></p>
        <p>Before I start each drawing, I look for inspiration. This can be from clothing textures, to random photos, or even other artists&rsquo; work. Sometimes I can get a bit lost, and before I know it, I have pinned a few hundred images and have drawn nothing. One artist that I really love is Ross Draws! His work always blows my mind, and though my art is different from his, he is currently my number One inspiration.&nbsp;</p>
        <p>After I have some inspiration, I do a few warm up pieces, while constantly flipping my canvas. If I ever feel as though I am getting nowhere, I open up Design Doll&mdash;a 3d modeling program created to help with poses. You get to play with a doll and move their body parts, similar to how you would with a plastic/wooden drawing figure doll, which I also sometimes use.</p>
        <p><strong>Who are your artistic influences?</strong></p>
        <p>I have a few, if I list all of them we would probably be here all day so I will just be listing my top favourite. This is in no order:&nbsp;Ross Draws,&nbsp;Kienan Lafferty,&nbsp;Mikey Mega Mega,&nbsp;Loish,&nbsp;Ilya Kuvshinov,&nbsp;Jetty Jet</p>
        <p><strong>What got you interested in NFTs/Crypto Art/Digital Collectibles?</strong></p>
        <p>A friend actually got me into this. It was on new years this year. Before then, I had no idea about all of this. I had heard of the word bitcoin tossed around but that was about it. After buying my first Kogs pack off the secondary market, I got all excited and wanted to learn more about this whole world. Now I am the nut job that constantly talks about NFT&rsquo;s.</p>
