        <h4>by Michael Macfadden</h4>
        <p>My name is Offroad and I live in the Bay Area, California. I&rsquo;ve been doing all kinds of art. I draw, paint, sculpt and animate, but I will focus on the folded paper since that&rsquo;s what the collaboration uses. Let me describe how I create these designs. I fold up paper with things tucked into the folds. Then I carefully pour in colors which flows around inside the folded paper. The folds create a Rorschach inkblot effects, and the things that I tuck into the folds act like stencils guiding the colors along their shapes. Some of the things I tuck into the paper can include pressed flowers, tiny gears from a watch, and even soap bubbles.</p>
        <p>Then I unfold the paper, rinse it off and my favorite part&hellip; I get to see the results. Anything and everything can effect the outcome &ndash; the pattern of the folds, the combination and the chemistry of the colors, the particular stencils, the timing, and even the temperature can effect the outcome. While it is chaotic, it is not completely random. I&rsquo;ve been doing this process for a while and I&rsquo;ve learned different techniques to get effects that I really like. I&rsquo;m also coming up with different ideas, new things to try &ndash; &ldquo;Hey, what if I dip the folded paper into water and freeze it before I add the color? Will it make an ice crystal pattern?&rdquo;</p>
        <p>I brought some of these folded paper designs into the computer and Ryan Medeiros created some very clean, modern frames, and animated them in After Effects. Ryan is a professional graphic designer with tons of industry experience, so I&rsquo;m really happy to have him doing this with me.</p>
        <p>Some of my artistic influences&hellip;. I like a lot of different stuff. I do like to think of the folded paper as surrealism. The surrealist would do different forms of automatic art, they would draw or write words without any conscious thought to it and just see what weird things came out. They used this to unlock their imagination. I do like to gaze at the folded paper in a similar way someone might gaze into a crystal ball, or scry with a dark mirror, observing the patterns and shapes that were created from the forces of the universe at that moment. It&rsquo;s like taking a cross section reading of reality.</p>
