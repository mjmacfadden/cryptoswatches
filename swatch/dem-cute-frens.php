        <p><strong>What got you interested in NFTs/Crypto Art/Digital Collectibles?</strong></p>
        <p>I follow a guy named BlenderGuru on Youtube; who has a series of tutorials and podcasts relating to 3D. Comments regarding Beeple&rsquo;s crazy sales on Nifty Gateway and how the NFT marketplaces were allowing 3D artists to sell their work struck a chord with me. I was already trying to sell some of my models online, but I had never sold anything to be honest, so I thought, &ldquo;maybe I should try this NFT thing?&rdquo;. This is how I started looking for marketplaces but the &ldquo;famous&rdquo; ones were quite expensive and hard to get in. Luckily, I finally found the WAX ecosystem and started collecting stuff there. A month later I did my first NFT and this is when my journey in this nice community began.</p>
