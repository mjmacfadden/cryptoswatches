        <p><strong>Please introduce yourself to our readers. What would you like them to know about you?</strong></p>
        <p>My name is Patrick, and I am a graphic and web designer currently living in Knoxville, TN. I heard about NTFs for the first time when recording an episode of the podcast&nbsp;<a title="For OR Against Podcast" href="https://fororagainstpod.com/" target="_blank" rel="noopener">For OR Against</a>. My co-host brought it up as a topic. There are four of us and we were pretty split on the idea of NTFs, but I was very intrigued and wanted to look closer. I bought into the wax ecosystem for the first time by buying some Street Fighter packs. After buying and trading for a bit a project came to me called NFT Stamps.</p>
        <p><strong>What was the inspiration for this piece?</strong></p>
        <p>The idea behind NFT Stamps is that there is a cost to interacting on the blockchain. Whether it is RAM, gas, or proof of work, there is a real world impact made by these digital items. NFT stamps represent not only the monetary cost creating, buying, and selling art, but also the environmental impact of powering the blockchain. Since my project is focusing on the environmental impact, 10% of profits will go towards buying carbon offsets.</p>
        <p><strong>How long have you been designing in the digital realm?</strong></p>
        <p>I have been doing some sort of digital design since high school so we are creeping up on 20 years.</p>
        <p><strong>Can you tell us about your workflow (how do you come up with ideas, what tools do you use to create)?</strong></p>
        <p>For this project I start with an idea of the stamp collection. So far I have old art and renewable energy. For old art I am playing off of the idea of NFTs ability to prove originality just like a 500 year old painting. So I find interesting public domain art and use both photoshop and After Effects to bring it to life. I have been playing around with creating different mouth shapes and eye movements. This way the art can pop off the screen.</p>
        <p><strong>Who are your artistic influences?</strong></p>
        <p>I am a fan of watching digital artists create art in photoshop. Not only to see what people can think up, but to come discover new techniques. I don&rsquo;t have any specific artists in mind because YouTube decides who I am going to watch next.</p>
        <p><strong>What got you interested in NFTs/Crypto Art/Digital Collectibles?</strong></p>
        <p>Buying and selling Street Fighter cards when they first came out got me interested. I really liked the idea that you could combine digital collectables together to create something new. This is something you could never do in the tangible world.</p>
